package hw4.player;

/**
 * Represents the different directions in which the player can move
 */
public enum Movement {
	
	/**
	 * The player wants to move in the direction of the cell to their left
	 */
	LEFT,
	
	/**
	 * The player wants to move in the direction of the cell to their right
	 */
	RIGHT,
	
	/**
	 * The player wants to move in the direction of the cell above
	 */
	UP,
	
	/**
	 * The player wants to move in the direction of the cell below
	 */
	DOWN

}
